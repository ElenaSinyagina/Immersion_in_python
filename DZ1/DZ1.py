from random import randint

num = randint(0, 1000)
count = 1
while count < 10:
    n = int(input('Угадайте число: '))
    if n == num:
        print('Угадали!')
        break
    elif n > num: print('Мое число меньше.', count + 1, 'попытка')
    else: print('Мое число больше.', count + 1, 'попытка')
