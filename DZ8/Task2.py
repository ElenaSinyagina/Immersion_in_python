import json


def getAccessLevel():
    accessLevels = ['1', '2', '3', '4', '5', '6', '7']

    with open('doc2.json', 'r', encoding='utf-8') as read_access:
        myDict = json.load(read_access)                             # –ø—Ä–æ—á–∏—Ç–∞—Ç—å –¥–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞
        print(myDict)
        read_access.close()

        while True:                                                 # –∑–∞–ø—É—Å–∏—Ç—å –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª: –ø–æ–∫–∞ —Å–ª–æ–≤–∞—Ä—å –µ—Å—Ç—å...
            ident = input("–≤–≤–µ–¥–∏—Ç–µ –ª–∏—á–Ω—ã–π –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä: ")
            if ident in myDict.keys() :                             # –µ—Å–ª–∏ –µ—Å—Ç—å –∏–¥–µ–Ω—Ç–∏—Ñ-—Ä –≤ –∫–ª—é—á–∞—Ö —Å–ª–æ–≤–∞—Ä—è, –Ω–∞–ø–∏—Å–∞—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ
                print("–∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä —É–∂–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è")
            else :
                while True:
                    access = input(f"–≤–≤–µ–¥–∏—Ç–µ —É—Ä–æ–≤–µ–Ω—å –¥–æ—Å—Ç—É–ø–∞ —Å {min(accessLevels)} –ø–æ {max(accessLevels)} : ")
                    if access not in accessLevels :
                        print ("–Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –≤–≤–æ–¥")
                    else :
                        myDict[ident] = access
                        sortedDict = dict(sorted(myDict.items(), key=lambda item: item[1], reverse=True))  # –µ—Å–ª–∏ reverse=True, —Ç–æ —ç–ª–µ–º–µ–Ω—Ç—ã —á–∏—Å–ª–æ–≤–æ–≥–æ —Å–ø–∏—Å–∫–∞ –æ—Ç—Å–æ—Ä—Ç –ø–æ —É–±—ã–≤–∞–Ω–∏—é 
                        print(sortedDict)

                        with open('doc2.json', 'w', encoding="utf-8") as write_access:
                            new_entry = json.dumps(sortedDict, indent=2, separators=(',', ':'))
                            write_access.write(new_entry)
                            write_access.close()
                            break
                break

getAccessLevel()